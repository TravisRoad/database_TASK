SELECT MAX(A.Traffic), MIN(A.Traffic), AVG(A.Traffic)
FROM user58db.tbcell_traffic AS A
	     INNER JOIN user58db.`1tbcell` AS B
	                ON A.Sector_ID = B.SECTOR_ID
WHERE (TIME(A.Date) BETWEEN "09:00" AND "11:00" OR
       TIME(A.Date) BETWEEN "19:00" AND "21:00")
  AND B.LATITUDE BETWEEN 0 AND 90
  AND B.LONGITUDE BETWEEN 100 AND 120
  AND DATE(A.Date) BETWEEN "01/05/2020" AND "31/05/2020"
GROUP BY A.SECTOR_ID

